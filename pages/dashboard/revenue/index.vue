      v-if="enrolled"
      ref="modal_transfer"
      :wallet="auth.user.payout_data.payout_wallet"
      :account-type="auth.user.payout_data.payout_wallet_type"
      :account="auth.user.payout_data.payout_address"
      :balance="auth.user.payout_data.balance"
